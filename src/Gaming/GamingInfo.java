    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        try {
            // file location
            File help = new File("src\\Gaming\\HELP.pdf");
            if (help.exists()) {
                if (Desktop.isDesktopSupported()) {
                    Desktop.getDesktop().open(help);
                } else {
                    JOptionPane.showMessageDialog(this, "file format not supported");
                }
            } else {
                JOptionPane.showMessageDialog(this, "file does not exist");
            }
        } catch (Exception e) {
            String Message = "File cant be opened";
            JOptionPane msg = new JOptionPane();
            msg.showMessageDialog(this, Message, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

